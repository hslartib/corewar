#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ar;
	size_t	i;

	ar = (char *)b;
	i = 0;
	while (i < len)
	{
		ar[i] = (char)c;
		i++;
	}
	return (b);
}
